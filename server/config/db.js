import mongoose from 'mongoose';

const connectDB = async () => {
    try {
        // We will use a local fallback if MONGO_URI is not provided initially
        const conn = await mongoose.connect(process.env.MONGO_URI || 'mongodb://127.0.0.1:27017/sentinel_grid');
        console.log(`MongoDB Connected: ${conn.connection.host}`);
    } catch (error) {
        console.error(`Error: ${error.message}`);
        process.exit(1);
    }
};

export default connectDB;
